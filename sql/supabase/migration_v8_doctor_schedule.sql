-- ── Table: doctor_schedule ───────────────────────────────────────────────────
-- Stores the fixed weekly working hours for each day of the week.
-- day_of_week: 0=Monday, 1=Tuesday, ..., 6=Sunday

-- Keep this migration self-contained for installations where the shared
-- trigger function from an earlier migration is not present.
CREATE OR REPLACE FUNCTION public.set_updated_at()
RETURNS trigger
LANGUAGE plpgsql
AS $$
BEGIN
    NEW.updated_at = timezone('utc', now());
    RETURN NEW;
END;
$$;

CREATE TABLE IF NOT EXISTS doctor_schedule (
    id              bigint generated by default as identity PRIMARY KEY,
    created_at      timestamptz DEFAULT timezone('utc', now()) NOT NULL,
    updated_at      timestamptz DEFAULT timezone('utc', now()) NOT NULL,
    day_of_week     integer     NOT NULL,
    day_name        text        NOT NULL,
    morning_start   time,
    morning_end     time,
    afternoon_start time,
    afternoon_end   time,
    is_active       boolean     NOT NULL DEFAULT true,
    CONSTRAINT doctor_schedule_day_unique UNIQUE (day_of_week),
    CONSTRAINT doctor_schedule_valid_day CHECK (day_of_week >= 0 AND day_of_week <= 6)
);

DROP TRIGGER IF EXISTS set_doctor_schedule_updated_at ON doctor_schedule;
CREATE TRIGGER set_doctor_schedule_updated_at
    BEFORE UPDATE ON doctor_schedule
    FOR EACH ROW EXECUTE FUNCTION public.set_updated_at();

