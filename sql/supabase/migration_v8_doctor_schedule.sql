-- ── Table: doctor_blocked_days ────────────────────────────────────────────────
-- Stores specific dates the doctor has blocked (emergency, vacation, leave).

CREATE TABLE IF NOT EXISTS doctor_blocked_days (
    id          bigint generated by default as identity PRIMARY KEY,
    created_at  timestamptz DEFAULT timezone('utc', now()) NOT NULL,
    blocked_date date        NOT NULL,
    reason      text        NOT NULL DEFAULT '',
    blocked_by  text        NOT NULL DEFAULT 'doctor',
    CONSTRAINT doctor_blocked_days_date_unique UNIQUE (blocked_date)
);

CREATE INDEX IF NOT EXISTS idx_doctor_blocked_days_date
    ON doctor_blocked_days (blocked_date);

